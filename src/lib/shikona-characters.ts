export function splitJapaneseName(value: string) {
  return Array.from(value.replace(/\s+/g, ""));
}

export function getCharacterStudyLabel(character: string) {
  return KANJI_MEANINGS[character] ?? KANA_ROMAJI[character] ?? "Dictionary";
}

export function getCharacterDictionaryUrl(character: string) {
  return `https://jisho.org/search/${encodeURIComponent(`${character} #kanji`)}`;
}
